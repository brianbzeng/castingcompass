#!/usr/bin/env node

import { createHash } from "node:crypto";
import {
  mkdtempSync,
  readFileSync,
  renameSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { basename, dirname, join, resolve } from "node:path";
import { tmpdir } from "node:os";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const UV_VERSION = "0.10.11";
const PYTHON_VERSION = "3.12.13";
const PYTHON_VERSION_FILES = [
  ".python-version",
  "services/api/.python-version",
  "pipeline/.python-version",
];
const VALIDATION_LOCK = "pipeline/requirements-validation.lock";
const VALIDATION_CONSTRAINTS = "pipeline/requirements-validation.txt";
const LOCKS = [
  {
    name: "FastAPI runtime",
    inputs: [
      ".python-version",
      "services/api/.python-version",
      "services/api/requirements.txt",
    ],
    source: "services/api/requirements.txt",
    output: "services/api/requirements-runtime.lock",
  },
  {
    name: "FastAPI test",
    inputs: [
      ".python-version",
      "services/api/.python-version",
      "services/api/requirements-test.in",
      "services/api/requirements.txt",
    ],
    source: "services/api/requirements-test.in",
    output: "services/api/requirements-test.lock",
  },
  {
    name: "pipeline CI",
    inputs: [
      ".python-version",
      "pipeline/.python-version",
      "pipeline/requirements-ci.in",
      "pipeline/requirements-smoke.txt",
      VALIDATION_LOCK,
      VALIDATION_CONSTRAINTS,
    ],
    source: "pipeline/requirements-ci.in",
    output: "pipeline/requirements-ci.lock",
  },
  {
    name: "Geo/deep macOS ARM64",
    inputs: [
      ".python-version",
      "pipeline/.python-version",
      "pipeline/requirements-geo-deep.in",
      "pipeline/requirements-smoke.txt",
      VALIDATION_LOCK,
      VALIDATION_CONSTRAINTS,
    ],
    source: "pipeline/requirements-geo-deep.in",
    output: "pipeline/requirements-geo-deep-macos-arm64.lock",
    target: "CPython 3.12 on macOS 15+ ARM64; PyPI wheels; Torch MPS when available.",
    pythonPlatform: "aarch64-apple-darwin",
    environment: { MACOSX_DEPLOYMENT_TARGET: "15.0" },
    defaultIndex: "https://pypi.org/simple",
  },
  {
    name: "Geo/deep Linux x86-64 CPU",
    inputs: [
      ".python-version",
      "pipeline/.python-version",
      "pipeline/requirements-geo-deep.in",
      "pipeline/requirements-smoke.txt",
      VALIDATION_LOCK,
      VALIDATION_CONSTRAINTS,
    ],
    source: "pipeline/requirements-geo-deep.in",
    output: "pipeline/requirements-geo-deep-linux-cpu.lock",
    target: "CPython 3.12 on manylinux_2_28 x86-64; official PyPI and PyTorch CPU wheels.",
    pythonPlatform: "x86_64-manylinux_2_28",
    torchBackend: "cpu",
    defaultIndex: "https://pypi.org/simple",
  },
];

function sha256(path) {
  return createHash("sha256").update(readFileSync(resolve(ROOT, path))).digest("hex");
}

function verifyPythonVersionFiles() {
  const expected = `${PYTHON_VERSION}\n`;
  for (const path of PYTHON_VERSION_FILES) {
    if (readFileSync(resolve(ROOT, path), "utf8") !== expected) {
      throw new Error(`${path} must select exact Python ${PYTHON_VERSION}`);
    }
  }
}

function verifyValidationConstraintMirror() {
  const canonical = readFileSync(resolve(ROOT, VALIDATION_LOCK));
  const dependabotMirror = readFileSync(resolve(ROOT, VALIDATION_CONSTRAINTS));
  if (!canonical.equals(dependabotMirror)) {
    throw new Error(`${VALIDATION_CONSTRAINTS} must exactly mirror ${VALIDATION_LOCK}`);
  }
}

function lockHeader(lock) {
  return [
    `# Generated by scripts/generate-python-locks.mjs with uv ${UV_VERSION}.`,
    `# Target: ${lock.target ?? "CPython 3.12 universal wheel resolution; source builds are disabled at install."}`,
    ...lock.inputs.map((path) => `# Input-SHA256: ${path}=${sha256(path)}`),
    "# Regenerate: node scripts/generate-python-locks.mjs --write",
    "",
  ].join("\n");
}

function requirementBlocks(body) {
  const starts = [...body.matchAll(/^[A-Za-z0-9][^\n]*$/gm)]
    .filter((match) => !match[0].startsWith("--"));
  return starts.map((match, index) => {
    const end = starts[index + 1]?.index ?? body.length;
    return body.slice(match.index, end);
  });
}

function verifyLock(lock) {
  const path = resolve(ROOT, lock.output);
  const content = readFileSync(path, "utf8");
  const header = lockHeader(lock);
  if (!content.startsWith(header)) throw new Error(`${lock.output} has stale generator or input metadata`);
  if (!content.endsWith("\n")) throw new Error(`${lock.output} must end with one newline`);
  if (/\s+@\s+|git\+|https?:\/\//i.test(content.slice(header.length))) {
    throw new Error(`${lock.output} must contain only indexed exact-version requirements`);
  }

  const blocks = requirementBlocks(content.slice(header.length));
  if (blocks.length < 2) throw new Error(`${lock.output} has no resolved dependency graph`);
  for (const block of blocks) {
    const firstLine = block.slice(0, block.indexOf("\n")).trimEnd();
    if (!/^[A-Za-z0-9][A-Za-z0-9._-]*==[^\s\\]+(?:\s*;[^\\]+)?\s*\\?$/.test(firstLine)) {
      throw new Error(`${lock.output} contains a non-exact requirement: ${firstLine}`);
    }
    if (!/--hash=sha256:[a-f0-9]{64}/.test(block)) {
      throw new Error(`${lock.output} contains an unhashed requirement: ${firstLine}`);
    }
    const hashFlags = block.match(/--hash=[^\s\\]+/g) ?? [];
    if (hashFlags.some((flag) => !/^--hash=sha256:[a-f0-9]{64}$/.test(flag))) {
      throw new Error(`${lock.output} contains a non-SHA-256 distribution hash: ${firstLine}`);
    }
  }
  return blocks.length;
}

function uvCommand(lock, output) {
  const command = [
    "pip",
    "compile",
    lock.source,
    "--python-version",
    "3.12",
    ...(lock.pythonPlatform ? ["--python-platform", lock.pythonPlatform] : ["--universal"]),
    "--generate-hashes",
    "--only-binary",
    ":all:",
    "--no-header",
    "--output-file",
    output,
  ];
  if (lock.torchBackend) command.push("--torch-backend", lock.torchBackend);
  if (lock.defaultIndex) command.push("--default-index", lock.defaultIndex);
  return command;
}

function verifyUvVersion() {
  const result = spawnSync("uv", ["--version"], { cwd: ROOT, encoding: "utf8" });
  if (result.status !== 0 || !new RegExp(`^uv ${UV_VERSION}(?:\\s|$)`).test(result.stdout)) {
    throw new Error(`Lock generation requires uv ${UV_VERSION} exactly`);
  }
}

function writeLocks() {
  verifyUvVersion();
  const temporaryDirectory = mkdtempSync(join(tmpdir(), "castingcompass-python-locks-"));
  try {
    for (const lock of LOCKS) {
      const generated = join(temporaryDirectory, basename(lock.output));
      const result = spawnSync("uv", uvCommand(lock, generated), {
        cwd: ROOT,
        encoding: "utf8",
        env: { ...process.env, UV_NO_PROGRESS: "1", ...lock.environment },
      });
      if (result.status !== 0) {
        process.stderr.write(result.stdout);
        process.stderr.write(result.stderr);
        throw new Error(`${lock.name} lock generation failed`);
      }
      const body = readFileSync(generated, "utf8").trimEnd();
      const destination = resolve(ROOT, lock.output);
      const temporaryDestination = `${destination}.tmp-${process.pid}`;
      writeFileSync(temporaryDestination, `${lockHeader(lock)}${body}\n`, { flag: "wx", mode: 0o600 });
      renameSync(temporaryDestination, destination);
    }
  } finally {
    rmSync(temporaryDirectory, { recursive: true, force: true });
  }
}

const mode = process.argv[2] ?? "--check";
if (mode !== "--check" && mode !== "--write") {
  throw new Error("Usage: generate-python-locks.mjs [--check|--write]");
}
verifyPythonVersionFiles();
verifyValidationConstraintMirror();
if (mode === "--write") writeLocks();

for (const lock of LOCKS) {
  console.log(`${lock.name} Python lock verified (${verifyLock(lock)} exact hashed packages).`);
}
